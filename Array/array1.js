let size=[38,40,42,44]
size.push(46);  //.push is used to add value/element to end of your array.
console.log(size);
//Output: [ 38, 40, 42, 44, 46 ]


let a=[38,40,42,44]
a.pop(); //.pop is used to delete value/element from end of your array.
console.log(a);
//Output: [ 38, 40, 42 ]


let x=[38,40,42,44]
x.unshift(36); //.unshift is used to add value/element to start of your array.
console.log(x);
//Output: [ 36, 38, 40, 42, 44 ]


let z=[38,40,42,44]
z.shift(); //.shift() is used to delete value/element from start of your array.
console.log(z);
//Output: [ 40, 42, 44 ]














